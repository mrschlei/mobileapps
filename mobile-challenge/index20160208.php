<a name="rules"></a>
<h2>Contest Rules</h2>

<h3>Eligibility</h3>

<p>Current <span class="nobr">U-M</span> students, faculty and staff are eligible to participate, including temporary employees. Teams comprised of eligible members are encouraged, although only one person per team may submit the application.</p>

<h3>Guidelines</h3>

<p>You may submit as many apps as you choose for iOS (iPhone, iPad or iPod touch), Android, Windows <span class="nobr">Phone </span> or other mobile Web apps. App ideas or concepts are not eligible for submission. Either your app must be available for download from an App Store or you must have a working prototype you can share with us. A panel of experts from <span class="nobr">U-M</span> and challenge sponsors will judge your app based on creativity, usability and utility.</p>



