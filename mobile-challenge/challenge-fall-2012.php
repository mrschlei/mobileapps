<p><strong>That Translation Game</strong></a><br/>
Through competitive play, contestants create and defend a translation of a given work (often a short piece of text or segment of video). Operating underneath this process is the rhetorical idea that the act of translation is not simply a binary correct/&#8203;incorrect process, and that contestants should move beyond the idea of a "perfect" translation. The act of spontaneous creation based on goals like target audience, tone, or register, coupled with the necessity to defend the translation to win the favor of the host or audience both support those goals. Wrapping that process within the framework of a game creates a fun atmosphere where the cost of failure is low, and students can have fun exploring a concept in an active, social environment.</p>
<p><em>Platform:</em> iOS</p>
<p><em>Team:</em> Johnathon Beals</p>
