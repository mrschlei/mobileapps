<h1>MiWorkspace Features and Benefits </h1>



<h2>Get Help at The "One-Stop" Service Center</h2>

<p>On the first day of your unit's transition to MiWorkspace, everyone should call the Service Center at 4-HELP (734-764-4357) for <span class="nobr">high-quality</span> service that is efficient, thorough, and consistent, for all of your general and desktop support questions.</p>

<div class="learnmore">
<h3>Learn More</h3>
<a class="modal-video" href="/learn/learn-more/archive/modal-video.php?v=OItgP0rOnRQ"><img src="/learn/images/video-how-to-get-help-thumbnail.jpg" alt="screenshot of the How to Get Help video" height="128" width="210"/></a><br/>
<p class="smallText">Learn more by viewing the <span class="nobr"><a  class="modal-video" href="/learn/learn-more/archive/modal-video.php?v=OItgP0rOnRQ">How to Ask For Help After Your Transition</a></span> infocast (2 minutes).</p>
</div>

<ul>
<li><p>Familiar faces on-site for desktop support help</p>
<ul>
<li><p>You will more than likely continue to see the same familiar faces in your unit ready to help you with any desktop question, but now they will be backed by an ever-expanding network of experts across campus, shared best-practices, and a growing database of knowledge.</p></li>
</ul></li>
<li><p>3 ways to ask for help</p>
<ul>
<li><p><strong>Call 4-HELP</strong> (734-764-4357) and speak to specially trained service agents. It is the fastest way to get your questions answered.</p></li>
<li><p><strong>Submit your question or issue online</strong> at <a href="http://servicerequest.umich.edu">servicerequest.umich.edu</a> and track its progress the same way you would track a UPS package.</p></li>
<li><p><strong>Use the self-service database feature</strong> and search for answers to commonly asked "how-to" questions by searching an online knowledge bank of articles.</p></li>
</ul></li>
<li><p>Transforming ITS into a knowledge-based organization</p>
<ul>
<li><p>Every call, issue, and solution will be thoroughly documented, creating a robust database of knowledge.</p></li>
<li><p>This information will be shared internally, among all Service Center agents, so when someone else calls in with a similar issue, they will get faster, more thorough service because a solution has already been documented.</p></li>
<li><p>Much of this information will also be shared publicly so anyone can search for solutions on their own.</p></li>
<li><p>Much of this information will also be shared publicly so anyone can search for solutions on their own.</p></li>
<li><p>With every call documented, you will only have to explain your issue one time to an agent, the first time you call. If any follow up is required, the next rep will be able to easily read the history.</p></li>

</ul></li>
</ul>


<h2>Access Your Files and Data Remotely</h2>

<ul>
<li><p>With any network connection, new storage locations such as Google Docs and M+Box will allow you to access and share your files from any location, even when you are mobile. Want to review a presentation on your mobile phone during soccer practice? Just sync it to your Box account and review it from the sidelines!</p></li>
<li><p>From your MiWorkspace laptop, you will always be able to access your files and data stored in your My Documents and your unit departmental shared drives from your MiWorkspace computer, whenever you are connected to the Internet. So if you take your computer home and get online, these files will be available to you without using a VPN client and without an additional log in.  Mac users will continue to use the Virtual Private Network (VPN) to access their home directory space (also referred to as your personal space on the network storage), and departmental shared drives.</p></li>
<li><p>50 GB of cloud-based storage in the M+Box account space &mdash; Box storage space is a cloud-based storage option for any non-sensitive or non-regulated data. It includes the ability to sync the "My Box Files" on your workspace to the cloud; you can download a Box document from other devices;  your files will automatically be backed-up, and you can access your files from anywhere you have a network/WiFi connection, including personal computers, smart phones, and tablets.</p></li>
<li><p>Additional personal storage: 5GB of personal storage for some types of sensitive or regulated data.</p> </li>
</ul>

<p>Learn more about selecting the <a href="http://www.itcs.umich.edu/itcsdocs/r1468/">best storage options</a> for your data and information.</p>


<h2>Software</h2>

<h3>MiApps - Virtual Software-on-Demand Available</h3>

<p>MiApps offers software-on-demand that can be accessed anytime, anyplace, and from many devices with an Internet connection. Many common Windows software titles are available through MiApps. MiApps will be installed on your MiWorkspace computer for easy access.</p>

<p>MiApps benefits include:</p>

<ul>
<li><p>For Windows and Mac, you can access select software titles that are only needed on occasion. Rather than installing the software locally on your MiWorkspace computer, use MiApps to launch the software and open, view, and edit files.</p></li>
<li><p>From your Mac, access Windows specific software, like Microsoft Visio and Project. You can open, view, and edit files without having to install virtual PC software on your computer.</p></li>
<li><p>Once an app has been launched, access files and data stored in your home directory (also referred to as your personal storage space on the network), and your units shared departmental drives using MiApps.</p></li>
</ul>

<p>If you do not have access to an app once you have transitioned to MiWorkspace, you can request it by contacting the Service Center at 4-HELP (734-764-4357).</p><br />


<h3>Software Available Through the New Michigan IT Services Portal (coming in September, 2012)</h3>

<p>Choose from many software titles available at the new Michigan IT Services portal. You can:</p>

<ul>
<li><p>Request software to be downloaded to your MiWorkspace computer.</p></li>
<li><p>Make a special request for software if it is not listed. In some instances, your supervisor's approval may be required.</p></li>
</ul>
<p>If you do not have access to an app once you have transitioned to MiWorkspace, you can request it by contacting the Service Center at 4-HELP (734-764-4357).</p><br />

<h2>Receive a New Managed Desktop for Windows and Mac</h2>

<ul>
<li><p>Updates for patches and other functional updates will be pushed to your computer; you will never have to worry whether or not you should accept them.</p></li>
<li><p>Plus, you can opt to defer an update if it interrupts your work and accept it at a later time.</p></li>
<li><p>For example, MiWorkspace will make sure all of the software installed on your computer will receive automatic updates; Mac and Windows users will no longer need to be concerned whether or not their computer is properly configured to receive updates; this will be taken care of for them.</p></li>
</ul>


<h2>Stay Connected</h2>

<ul>
<li><p>Mobile and remote access will be available from your university-owned or personal device. For example, you can access files stored on M+Box and M+Google from your mobile device such as an iPhone. This does not apply to network drives or files stored only locally on your computer.</p></li>
<li><p>You will continue to have both wired and wireless network access.</p></li>
<li><p>Stay connected on campus even when moving from building to building through MWireless – a secure and encrypted network that can protect your personal data as you move about campus.</p></li>
<li><p>The long-term goal is to build a network that is accessible anytime, anywhere on the University of Michigan campus. This will be possible as more units across campus adopt MiWorkspace.</p></li>
</ul><br />


<h2>Print Options</h2>

<p>MiWorkspace printing gives you options to print to a nearby printer in your unit, a network printer across campus, and wirelessly from your laptop.</p>

<ul>
<li><p>Print from any computer or mobile device to any MiWorkspace network printer across campus. Visit <a href="https://mprint.umich.edu/info">https://mprint.umich.edu/info</a> for a list all printers and more information about them.</p></li>
<li><p>Our new "Follow Me" feature offers secure printing to the cloud that can be released to any MiWorkspace multi-function printer equipped with a card reader. This printer will allow you to securely send a document to a print queue. To release your document to print, simply swipe your MCard or log in using your uniqname and UMICH password.</p></li>
<li><p>Just like today, you will have full-service printing available from your workspace with many new options including: viewing your environmental impact; ability to charge to multiple accounts; see your print history; and install printers seamlessly from the web.</p></li>
<li><p>You will continue to have access to MPrint - U-M's web-based printing service that allows campus community members to print from their computer to any MiWorkspace networked campus printer. MPrint is available to desktop, laptop, and mobile users.</p></li>
</ul>


<h2>Available Special Orders</h2>
<p>Order special hardware or software, above and beyond what is included in the service, for any special requests (e.g., higher capacity workstations). Knowledgeable service agents are available at 4-HELP (734-764-4357) to assist with any planning and logistical needs.</p><br /> 


<h2>Safe Computing</h2>

<p>MiWorkspace devices and computing environments are designed to ensure that the data, devices, and personal information of the members of the university community are safe and secure.  The security measures extend into back-end systems, as well, ensuring that your entire computing experience is safe at its source. By combining technology with best computing practices, you can protect  yourself and the University.</p>

<ul>
<li><p>Encrypted  laptops protect data in event of theft or loss</p></li>
<li><p>File scans locate sensitive data such as social security numbers on personal computers and shared infrastructure</p></li>
<li><p>Device and system scans identify potential vulnerabilities that result in patches and other safeguards to reduce risk</p></li>
<li><p>Secure configurations on laptop and desktops limit unauthorized access</p></li>
<li><p>Network monitoring identifies security threats in real-time, protecting connected devices against viruses and other threats</p></li>
<li><p>Some storage options in MiWorkspace will safely house most types of sensitive regulated data, including HIPAA. (HIPAA service begins Fall 2012)</p></li>
</ul>



