<h1>Grade Alerts - Android</h1>

</div>


<div data-role="content" data-theme="b">

<h2>Objective</h2>

<p>
Enabling grade alert push notifications in a test or live environment
</p>

<h2>Environment</h2>

<p>
Android OS
</p>

<h2>Procedure</h2>

<p>
If a user's device is not receiving grade alerts, there are several possible solutions.  Generally, these apply to live grade alerts and also the app's "Test Grade Alerts" feature.
</p>

<ol>
<li>Update to the latest version of the Android Michigan App via the Play store.</li>
<li>Allow push notifications to be sent to the device.  This can be found in the device Settings application.</li>
<li>Opt in to grade alerts within the Academics - Grade Alerts tool.</li>
<li>Device registration can take several minutes.  If the device was just registered, wait ~5 minutes and try again.  This most likely will arise while testing grade alerts since device registration typically occurs hours or days (or more) before live grade alerts are sent.</li>
<li>Delete the app from the device, reinstall via the Play store, and try the process again.</li>
<li>Contact the mobile team for additional diagnostic troubleshooting.  You can reach the team at <a href="mailto:its-michigan-app-feedback@umich.edu">its-michigan-app-feedback@umich.edu</a>.</li>
</ol>


<h2>Additional Notes</h2>

<p>
There is a known Android device in the Mobile Portal Web department that is set up correctly but to which we can not push grade alert notifications.  The mobile developers' theory is that it conflicts with another app on the device, but that is not definitive.  We do not know of other devices that share the same issue.
</p>


